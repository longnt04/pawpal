"use client";

import Image from "next/image";
import { format } from "date-fns";
import { useState } from "react";
import { IoArrowUndoOutline } from "react-icons/io5";

interface Message {
  id: string;
  content: string | null;
  image_url: string | null;
  sender_pet_id: string;
  reply_to_message_id: string | null;
  is_read: boolean;
  created_at: string;
  sender: {
    id: string;
    name: string;
    avatar_url: string | null;
  };
  replied_message?: {
    id: string;
    content: string | null;
    image_url: string | null;
    sender_pet_id: string;
    sender: {
      name: string;
    };
  } | null;
}

interface MessageBubbleProps {
  message: Message;
  isOwnMessage: boolean;
  showAvatar?: boolean;
  showTimestamp?: boolean;
  currentUserId: string | null;
  reactions: Array<{ user_id: string; reaction: string }>;
  onReaction: (messageId: string, reaction: string) => void;
  onReply: (message: Message) => void;
  onScrollToMessage?: (messageId: string) => void;
}

const REACTIONS = ["ğŸ‘", "â¤ï¸", "ğŸ˜‚", "ğŸ˜®", "ğŸ˜¢"];

export default function MessageBubble({
  message,
  isOwnMessage,
  showAvatar = true,
  showTimestamp = true,
  currentUserId,
  reactions,
  onReaction,
  onReply,
  onScrollToMessage,
}: MessageBubbleProps) {
  const [showReactions, setShowReactions] = useState(false);

  // Group reactions by emoji
  const reactionCounts: Record<
    string,
    { count: number; userReacted: boolean }
  > = {};
  reactions.forEach((r) => {
    if (!reactionCounts[r.reaction]) {
      reactionCounts[r.reaction] = { count: 0, userReacted: false };
    }
    reactionCounts[r.reaction].count++;
    if (r.user_id === currentUserId) {
      reactionCounts[r.reaction].userReacted = true;
    }
  });

  return (
    <div
      className={`flex items-end gap-2 ${showTimestamp ? "mb-4" : "mb-1"} ${
        isOwnMessage ? "flex-row-reverse" : "flex-row"
      }`}
    >
      {!isOwnMessage && showAvatar && (
        <Image
          src={message.sender.avatar_url || "https://via.placeholder.com/32"}
          alt={message.sender.name}
          width={32}
          height={32}
          className="rounded-full object-cover max-w-[32px] max-h-[32px] mb-5"
        />
      )}
      {!isOwnMessage && !showAvatar && <div className="w-8 h-8" />}
      <div
        className={`max-w-[70%] ${
          isOwnMessage ? "items-end" : "items-start"
        } flex flex-col relative`}
        onMouseEnter={() => setShowReactions(true)}
        onMouseLeave={() => setShowReactions(false)}
      >
        {/* Reaction Picker */}
        {showReactions && (
          <div
            className={`absolute -top-8 ${isOwnMessage ? "right-0" : "left-0"} bg-white shadow-lg rounded-full px-2 py-1 flex gap-1 border border-gray-200 z-10`}
          >
            {REACTIONS.map((emoji) => (
              <button
                key={emoji}
                onClick={() => onReaction(message.id, emoji)}
                className="hover:scale-125 transition-transform text-lg"
              >
                {emoji}
              </button>
            ))}
            <button
              onClick={() => onReply(message)}
              className="hover:scale-110 transition-transform text-gray-600 ml-1"
              title="Tráº£ lá»i"
            >
              <IoArrowUndoOutline size={18} />
            </button>
          </div>
        )}

        {/* Chá»‰ cÃ³ áº£nh - khÃ´ng gradient */}
        {message.image_url && !message.content && !message.replied_message ? (
          <div className="rounded-2xl overflow-hidden">
            <Image
              src={message.image_url}
              alt="Attached image"
              width={300}
              height={300}
              className="rounded-2xl object-cover"
            />
          </div>
        ) : (
          <>
            {/* áº¢nh riÃªng - khÃ´ng cÃ³ background */}
            {message.image_url && (
              <div className="mb-2 rounded-2xl overflow-hidden">
                <Image
                  src={message.image_url}
                  alt="Attached image"
                  width={300}
                  height={300}
                  className="rounded-lg object-cover"
                />
              </div>
            )}
            {/* Text hoáº·c reply - cÃ³ background */}
            {(message.content || message.replied_message) && (
              <div
                className={`rounded-2xl px-4 py-2 ${
                  isOwnMessage
                    ? "bg-gradient-to-r from-pink-500 to-purple-600 text-white"
                    : "bg-white border border-gray-200 text-gray-900 shadow-sm"
                }`}
              >
                {/* Replied Message Preview */}
                {message.replied_message && (
                  <div
                    onClick={() =>
                      onScrollToMessage?.(message.reply_to_message_id!)
                    }
                    className={`mb-2 pl-3 border-l-2 ${
                      isOwnMessage ? "border-white/50" : "border-pink-500"
                    } py-1 cursor-pointer hover:opacity-80 transition-opacity`}
                  >
                    <p
                      className={`text-xs ${
                        isOwnMessage ? "text-white/80" : "text-gray-600"
                      } font-medium`}
                    >
                      {message.replied_message.sender.name}
                    </p>
                    <div className="flex items-center gap-2">
                      {message.replied_message.image_url && (
                        <Image
                          src={message.replied_message.image_url}
                          alt="Replied image"
                          width={40}
                          height={40}
                          className="rounded object-cover min-w-[40px] min-h-[40px]"
                        />
                      )}
                      <p
                        className={`text-sm ${
                          isOwnMessage ? "text-white/90" : "text-gray-700"
                        } truncate flex-1`}
                      >
                        {message.replied_message.content}
                      </p>
                    </div>
                  </div>
                )}
                {message.content && (
                  <p className="break-words whitespace-pre-wrap">
                    {message.content}
                  </p>
                )}
              </div>
            )}
          </>
        )}

        {/* Reactions Display */}
        {Object.keys(reactionCounts).length > 0 && (
          <div
            className={`flex gap-1 mt-1 ${
              isOwnMessage ? "justify-end" : "justify-start"
            }`}
          >
            {Object.entries(reactionCounts).map(
              ([emoji, { count, userReacted }]) => (
                <button
                  key={emoji}
                  onClick={() => onReaction(message.id, emoji)}
                  className={`flex items-center gap-1 px-2 py-0.5 rounded-full text-xs ${
                    userReacted
                      ? "bg-pink-100 border border-pink-300"
                      : "bg-gray-100 border border-gray-200"
                  } hover:scale-110 transition-transform`}
                >
                  <span>{emoji}</span>
                  <span className="text-gray-700">{count}</span>
                </button>
              ),
            )}
          </div>
        )}

        {showTimestamp && (
          <span className="text-xs text-gray-500 mt-1 px-2">
            {format(new Date(message.created_at), "HH:mm")}
          </span>
        )}
      </div>
    </div>
  );
}
